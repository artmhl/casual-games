// ─── Result / Reveal Screen ───────────────────────────────────────────────

function ResultPhase({ players, roundData, mode, accusedIdx, onRestart }) {
  const accused = roundData.assignments[accusedIdx];
  const accusedIsSpy = accused.isSpy;
  const correctLocation = roundData.location.name;
  const modeInfo = MODE_INSTRUCTIONS[mode];

  // Check if there are ANY spies in the game (for mode 4 zero-spy scenario)
  const totalSpies = roundData.assignments.filter(a => a.isSpy).length;
  const isZeroSpyGame = totalSpies === 0;
  const isAllSpyGame = totalSpies === players.length;

  const [guessPhase, setGuessPhase] = useState(false);
  const [spyGuess, setSpyGuess] = useState(null);
  const [finalResult, setFinalResult] = useState(() => {
    if (isZeroSpyGame) return "no_spies"; // nobody is spy — special result
    if (!accusedIsSpy) return "wrong_accused";
    return null; // spy found — proceed to guess
  });

  const locationChoices = (() => {
    const pool = shuffle(LOCATIONS.filter(l => l.name !== correctLocation)).slice(0, 9);
    if (mode === 3 && roundData.locationA && !pool.find(l => l.name === roundData.locationA.name)) {
      pool[0] = roundData.locationA;
    }
    pool.push(roundData.location);
    return shuffle(pool);
  })();

  function handleGuess(locName) {
    setSpyGuess(locName);
    const isCorrect =
      locName === correctLocation ||
      (mode === 3 && roundData.locationA && locName === roundData.locationA.name);
    setFinalResult(isCorrect ? "spy_win" : "players_win");
  }

  // ── FINAL RESULT ──
  if (finalResult) {
    const spyWon = finalResult === "spy_win";
    const noSpies = finalResult === "no_spies";
    const wrongAccused = finalResult === "wrong_accused";

    let resultIcon = spyWon ? "🕵️" : noSpies ? "🕊️" : "🏆";
    let resultColor = spyWon ? "#f87171" : noSpies ? "#60a5fa" : "#4ade80";
    let resultText = spyWon ? "Шпигун переміг!" : noSpies ? "Шпигунів не було!" : "Гравці перемогли!";
    let resultDesc = "";
    if (spyWon && spyGuess) resultDesc = `Шпигун вгадав: «${spyGuess}» ✓`;
    if (wrongAccused && !noSpies) resultDesc = `Ви обвинуватили ${accused.name} — але він(вона) не шпигун!`;
    if (finalResult === "players_win") resultDesc = "Шпигун не вгадав локацію!";
    if (noSpies) resultDesc = `Ніхто не був шпигуном! ${accused.name} невинний(на). Це була перемога чесності 🌟`;

    return (
      <div style={{ display: "flex", flexDirection: "column", gap: 20 }}>
        <div style={{
          ...S.card, textAlign: "center", padding: "36px 20px",
          background: spyWon ? "linear-gradient(135deg, rgba(239,68,68,0.13), rgba(251,191,36,0.06))" : noSpies ? "linear-gradient(135deg, rgba(96,165,250,0.13), rgba(124,106,247,0.06))" : "linear-gradient(135deg, rgba(34,197,94,0.13), rgba(124,106,247,0.06))",
          border: `1px solid ${spyWon ? "rgba(239,68,68,0.45)" : noSpies ? "rgba(96,165,250,0.45)" : "rgba(34,197,94,0.45)"}`,
        }}>
          <div style={{ fontSize: "4rem", marginBottom: 12 }}>{resultIcon}</div>
          <div style={{ fontSize: "1.8rem", fontWeight: 900, marginBottom: 10, color: resultColor }}>{resultText}</div>
          <p style={{ ...S.muted, fontSize: "0.9rem", lineHeight: 1.65 }}>{resultDesc}</p>
        </div>

        {/* All roles revealed */}
        <div style={S.card}>
          <p style={{ fontWeight: 700, marginBottom: 12 }}>🔓 Всі ролі розкрито</p>
          <div style={{ display: "flex", flexDirection: "column", gap: 8 }}>
            {roundData.assignments.map((a, i) => (
              <div key={i} style={{
                display: "flex", alignItems: "center", gap: 12, padding: "10px 14px",
                borderRadius: "var(--radius-sm)",
                background: a.isSpy ? "rgba(239,68,68,0.09)" : "var(--bg3)",
                border: `1px solid ${a.isSpy ? "rgba(239,68,68,0.28)" : "var(--bg3)"}`,
              }}>
                <span style={{ fontSize: "1.2rem" }}>{a.isSpy ? "🕵️" : "🧑"}</span>
                <div style={{ flex: 1, minWidth: 0 }}>
                  <div style={{ fontWeight: 700 }}>{a.name}</div>
                  <div style={{ fontSize: "0.78rem", ...S.muted }}>
                    {a.isSpy ? "Шпигун" : `${a.role} · ${a.location}`}
                    {a.team === "A" ? " 🟡" : a.team === "B" ? " 🟣" : ""}
                  </div>
                </div>
                {i === accusedIdx && <span style={{ fontSize: "0.72rem", color: "#fbbf24", flexShrink: 0 }}>🎯 обвинувачений</span>}
              </div>
            ))}
          </div>
          <div style={{ marginTop: 14, padding: "12px 14px", background: "rgba(124,106,247,0.09)", borderRadius: "var(--radius-sm)", border: "1px solid rgba(124,106,247,0.22)" }}>
            <div><span style={S.accent}>📍 Основна локація: </span><strong>{correctLocation}</strong></div>
            {mode === 3 && roundData.locationA && (
              <div style={{ marginTop: 4 }}><span style={S.accent}>📍 Локація команди А: </span><strong>{roundData.locationA.name}</strong></div>
            )}
            {mode === 4 && (
              <div style={{ marginTop: 4, fontSize: "0.82rem", color: "#f472b6" }}>
                🎲 Шпигунів у грі було: <strong>{totalSpies}</strong> з {players.length}
              </div>
            )}
          </div>
        </div>

        <button className="btn-primary" onClick={onRestart} style={{ background: modeInfo?.color }}>🔄 Нова гра</button>
      </div>
    );
  }

  // ── SPY GUESS PHASE ──
  if (guessPhase) {
    return (
      <div style={{ display: "flex", flexDirection: "column", gap: 20 }}>
        <div style={{ textAlign: "center", paddingTop: 8 }}>
          <div style={{ fontSize: "2.8rem", marginBottom: 8 }}>🕵️</div>
          <h2 style={{ fontSize: "1.4rem", fontWeight: 900 }}>Шпигуна розкрито!</h2>
          <p style={{ ...S.muted, fontSize: "0.87rem", marginTop: 6, lineHeight: 1.5 }}>
            <strong style={{ color: "var(--text)" }}>{accused.name}</strong>, останній шанс:<br />
            вгадай локацію і перемагаєш!
            {mode === 3 ? " Правильна будь-яка з двох локацій!" : ""}
            {isAllSpyGame ? " (Всі були шпигунами — але ти все одно вгадуй!)" : ""}
          </p>
        </div>
        <div style={S.card}>
          <p style={{ fontWeight: 700, marginBottom: 12 }}>Вибери локацію:</p>
          <div style={{ display: "flex", flexDirection: "column", gap: 8 }}>
            {locationChoices.map((loc, i) => {
              const isCorrect = loc.name === correctLocation || (mode === 3 && roundData.locationA && loc.name === roundData.locationA.name);
              const isGuessed = spyGuess === loc.name;
              return (
                <button key={i} onClick={() => !spyGuess && handleGuess(loc.name)} disabled={!!spyGuess} style={{
                  display: "flex", alignItems: "center", gap: 12, textAlign: "left",
                  background: isGuessed ? (isCorrect ? "rgba(34,197,94,0.14)" : "rgba(239,68,68,0.1)") : (spyGuess && isCorrect ? "rgba(34,197,94,0.07)" : "var(--bg3)"),
                  border: `1px solid ${isGuessed ? (isCorrect ? "rgba(34,197,94,0.5)" : "rgba(239,68,68,0.35)") : (spyGuess && isCorrect ? "rgba(34,197,94,0.4)" : "var(--bg3)")}`,
                  borderRadius: "var(--radius-sm)", padding: "13px 16px",
                  cursor: spyGuess ? "default" : "pointer", color: "var(--text)", width: "100%", transition: "all 0.2s",
                }}>
                  <span>📍</span>
                  <span style={{ fontWeight: 600 }}>{loc.name}</span>
                  {spyGuess && isCorrect && <span style={{ marginLeft: "auto", color: "#4ade80" }}>✓ Правильно!</span>}
                  {isGuessed && !isCorrect && <span style={{ marginLeft: "auto", color: "#f87171" }}>✗ Невірно</span>}
                </button>
              );
            })}
          </div>
        </div>
      </div>
    );
  }

  // ── SPY FOUND — offer guess ──
  return (
    <div style={{ display: "flex", flexDirection: "column", gap: 20 }}>
      <div style={{ ...S.card, textAlign: "center", padding: "36px 20px", background: "linear-gradient(135deg, rgba(239,68,68,0.1), rgba(251,191,36,0.05))", border: "1px solid rgba(239,68,68,0.38)" }}>
        <div style={{ fontSize: "3.5rem", marginBottom: 10 }}>🎯</div>
        <div style={{ fontSize: "1.6rem", fontWeight: 900, marginBottom: 10 }}>Шпигуна знайдено!</div>
        <p style={{ ...S.muted, lineHeight: 1.65 }}>
          <strong style={{ color: "var(--text)", fontSize: "1.1rem" }}>{accused.name}</strong> — шпигун!<br />
          Але є останній шанс врятуватися...
        </p>
        {isAllSpyGame && (
          <div style={{ marginTop: 14, padding: "10px 14px", background: "rgba(236,72,153,0.1)", borderRadius: "var(--radius-sm)", border: "1px solid rgba(236,72,153,0.3)", fontSize: "0.83rem", color: "#f472b6" }}>
            🎲 Увага: у цій грі ВСІ були шпигунами!
          </div>
        )}
      </div>
      <div style={{ ...S.card, textAlign: "center", padding: "18px 20px" }}>
        <p style={{ fontWeight: 700, marginBottom: 6 }}>Шпигуне, це твій момент!</p>
        <p style={{ ...S.muted, fontSize: "0.87rem", lineHeight: 1.5 }}>
          Вгадай правильну локацію — і ти перемагаєш навіть після розкриття!
          {mode === 3 ? " Достатньо вгадати ОДНУ з двох локацій." : ""}
        </p>
      </div>
      <button className="btn-primary" onClick={() => setGuessPhase(true)}>🗺️ Вгадати локацію</button>
    </div>
  );
}

