// ─── Утиліти ──────────────────────────────────────────────────────────────

function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function pick(arr) { return arr[Math.floor(Math.random() * arr.length)]; }

function getRoleName(r) { return typeof r === "string" ? r : r.name; }
function getRoleHint(r) { return typeof r === "string" ? null : r.hint; }

function generateRound(players, mode) {
  const location = pick(LOCATIONS);
  const shuffledRoles = shuffle(location.roles);
  const shuffledIndices = shuffle(players.map((_, i) => i));

  let spyIndices = [];
  let locationA = null;
  let mode4SpyCount = 1;

  if (mode === 4) {
    // Random spy count: 0, 1, 1, 2, or all (weighted toward 1)
    const n = players.length;
    const pool = [0, 1, 1, 1, 2];
    if (n >= 4) pool.push(n); // all-spy only for 4+ players
    mode4SpyCount = pick(pool);
    spyIndices = shuffledIndices.slice(0, mode4SpyCount);
  } else if (mode === 3) {
    spyIndices = [shuffledIndices[0], shuffledIndices[1]];
    locationA = pick(LOCATIONS.filter(l => l.name !== location.name));
  } else {
    spyIndices = [shuffledIndices[0]];
  }

  const nonSpyIndices = shuffledIndices.filter(i => !spyIndices.includes(i));
  const teamASize = mode === 3 ? Math.floor(nonSpyIndices.length / 2) : 0;
  const teamAIndices = nonSpyIndices.slice(0, teamASize);

  const assignments = players.map((name, idx) => {
    const isSpy = spyIndices.includes(idx);

    if (mode === 3) {
      if (isSpy) return { name, isSpy: true, role: "Шпигун", roleHint: null, location: null, gadget: null, team: "spy" };
      const isTeamA = teamAIndices.includes(idx);
      const loc = isTeamA ? locationA : location;
      const r = shuffledRoles[idx % shuffledRoles.length];
      return { name, isSpy: false, role: getRoleName(r), roleHint: getRoleHint(r), location: loc.name, gadget: null, team: isTeamA ? "A" : "B" };
    }

    if (isSpy) {
      return {
        name, isSpy: true, role: "Шпигун", roleHint: null, location: null,
        gadget: mode === 2 ? pick(SPY_GADGETS) : null, team: "spy",
      };
    }

    const r = shuffledRoles[idx % shuffledRoles.length];
    return {
      name, isSpy: false,
      role: getRoleName(r), roleHint: getRoleHint(r),
      location: location.name,
      gadget: mode === 2 ? pick(GADGETS) : null,
      team: null,
    };
  });

  return { location, assignments, locationA, mode4SpyCount };
}

